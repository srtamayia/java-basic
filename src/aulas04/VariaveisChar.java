package aulas04;

public class VariaveisChar {

	public static void main(String[] args) {

		// tabela Unicode
		char interrogacao = 0x003f;		
				
		// tabela ASCII
		char o = 'o';
		char i = 'i';
		
		char o1 = 111;
		char i1 = 105;
		
		System.out.println("" + o + i + interrogacao);
		System.out.println("" + o1 + i1 + interrogacao);
		
	}

}
