package aulas05;

public class VariaveisBoolean {
	
	public static void main(String[] args) {
		
		// boolean
		int num = 0;
		boolean verdadeiro = true;
		boolean falso = false;
		
		if (num == 1) {
			System.out.println("" + verdadeiro);
		} else {
			System.out.println("" + falso);
		}
		
	}
	
}
